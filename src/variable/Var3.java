package variable;

public class Var3 {
    public static void main(String[] args) {
        //변수 선언 : 메모리 공간을 확보 하여 데이터 저장, 변수의 이름을 통하여 메모리 공간에 접근.
        int a;
        a = 10;
        System.out.println("a = " + a);
        
        a = 50;// 변수 값 변경 a (10 -> 50)
        System.out.println("a = " + a);
    }
}
